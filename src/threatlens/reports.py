"""Markdown threat report generator.

Produces comprehensive, human-readable reports combining analysis from
all four frameworks (STRIDE, DREAD, LINDDUN, PASTA) with CWE/MITRE
cross-references and prioritised mitigations.
"""

from __future__ import annotations

from datetime import datetime, timezone
from typing import TYPE_CHECKING

from threatlens.models import (
    AttackNode,
    AttackTree,
    DreadScore,
    FrameworkCorrelation,
    GateType,
    PastaStageResult,
    PrivacyImpact,
    Threat,
)

if TYPE_CHECKING:
    from collections.abc import Sequence


def _severity_badge(severity: str) -> str:
    badges = {
        "critical": "🔴 CRITICAL",
        "high": "🟠 HIGH",
        "medium": "🟡 MEDIUM",
        "low": "🟢 LOW",
        "info": "🔵 INFO",
    }
    return badges.get(severity, severity.upper())


def _dread_bar(score: float) -> str:
    filled = round(score)
    return "█" * filled + "░" * (10 - filled)


def _render_attack_node(node: AttackNode, depth: int = 0) -> list[str]:
    indent = "  " * depth
    gate_label = f" [{node.gate.value}]" if node.gate != GateType.LEAF else ""
    prob = f" (p={node.likelihood:.0%})" if node.likelihood else ""
    imp = f" [impact={node.impact}]" if node.impact else ""
    lines = [f"{indent}- {node.label}{gate_label}{prob}{imp}"]
    for child in node.children:
        lines.extend(_render_attack_node(child, depth + 1))
    return lines


